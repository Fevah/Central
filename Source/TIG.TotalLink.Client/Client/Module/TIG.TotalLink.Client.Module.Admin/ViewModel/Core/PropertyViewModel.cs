using DevExpress.Entity.Model;
using DevExpress.Mvvm.DataAnnotations;
using TIG.TotalLink.Client.Core.ViewModel;
using TIG.TotalLink.Client.Editor.Builder;
using TIG.TotalLink.Shared.DataModel.Core.Attribute;

namespace TIG.TotalLink.Client.Module.Admin.ViewModel.Core
{
    [DisplayField("Name")]
    public class PropertyViewModel : LocalDataObjectBase
    {
        #region Constructors

        public PropertyViewModel()
        {
        }

        public PropertyViewModel(IEdmPropertyInfo property)
            : this()
        {
            Property = property;
        }

        #endregion


        #region Public Properties

        /// <summary>
        /// The property that this view model contains.
        /// </summary>
        public IEdmPropertyInfo Property { get; private set; }

        /// <summary>
        /// The display name for the property.
        /// </summary>
        public string Name
        {
            get { return Property.DisplayName; }
        }

        #endregion
        

        #region Metadata

        /// <summary>
        /// Builds metadata for properties.
        /// </summary>
        /// <param name="builder">The MetadataBuilder for defining property metadata.</param>
        public static void BuildMetadata(MetadataBuilder<PropertyViewModel> builder)
        {
            builder.Property(p => p.Property).NotAutoGenerated();
        }

        /// <summary>
        /// Builds metadata for editors.
        /// </summary>
        /// <param name="builder">The EditorMetadataBuilder for defining editor metadata.</param>
        public static void BuildEditorMetadata(EditorMetadataBuilder<PropertyViewModel> builder)
        {
            builder.Sort()
                .ContainsProperty(p => p.Name);
        }

        #endregion
    }
}
